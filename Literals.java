public class Literals {
  public static void main(String[] args) {
    // Primitive DataType

    // Integral
    byte num1 = 34; // The byte keyword is a data type that can store whole numbers from -128 to 127.
    int num2 = 56;
    short num3 = 87;
    long num4 = 56666666666L;

    // Floats (Decimal)
    float f1 = 5.6f; // The float keyword is a data type that can store fractional numbers from 3.4e−038 to 3.4e+038.
    double d1 = 4.66d; // The double keyword is a data type that can store fractional numbers from 1.7e−308 to 1.7e+308.

    // Char ('A')
    char ch = 'A'; // Char keyword is a data type that is used to store a single character, It must be surrounded by single quotes, like 'A' or 'c'.

    // Boolean (true/false)
    boolean myth = true; // Boolean is a data type that is used to represent a single true/false value.
    String str = "Mythpat";
    System.out.println(num1);
  }
}
// In this program DataType, Variable, Literal is defined as:
// --------------------------------------------------
// |       DataType | Variable |            Literal |
// |           byte |     num1 |                 34 |
// |            int |     num2 |                 56 |
// |          short |     num3 |                 87 |
// |           long |     num4 |       56666666666L |
// |          float |       f1 |               5.6f |
// |         double |       d1 |              4.66d |
// |           char |       ch |                  A |
// |        boolean |     myth |               true |
// |         String |      str |            Mythpat |
// --------------------------------------------------
